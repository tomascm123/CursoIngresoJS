function mostrar()
{

	var contador=0;
	var acumulador=0;
	var respuesta='si';


	while 	(contador!=respuesta)			{

			acumulador = prompt("Ingrese un numero");

			acumulador = parseInt(acumulador);


}






document.getElementById('suma').value=acumulador;
document.getElementById('promedio').value=acumulador/contador;

}